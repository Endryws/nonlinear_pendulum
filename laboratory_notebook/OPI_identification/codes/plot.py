# =========================================================================== #
# =========================== Importing Libraries =========================== #
# =========================================================================== #


import numpy as np
import matplotlib.pyplot as plt
import matplotlib as mpl


# =========================================================================== #
# ===================== Defining the graphs parameteres ===================== #
# =========================================================================== #


plt.rcParams["font.family"] = "Times New Roman"

mpl.rc('xtick', labelsize=22)
mpl.rc('ytick', labelsize=22)


# =========================================================================== #
# ==================== Open the file with all the data ====================== #
# =========================================================================== #


name = "state_space1.10509496686"		# File name

data = np.genfromtxt(name + ".txt", skip_header=9, unpack=True)


# =========================================================================== #
# ====================== Generating the plot figure ========================= #
# =========================================================================== #


fig = plt.figure()

# Ploting the first and the second columm of the text file

plt.plot(data[0], data[1], 'bo', ms=1)


# =========================================================================== #
# ==================== Saving figure with the name alias ==================== #
# =========================================================================== #

fig.savefig(name + ".jpg")
